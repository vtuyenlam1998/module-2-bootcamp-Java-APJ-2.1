public class Main {
    public static void main(String[] args) {
        int i = 10; //khai báo biến i là kiểu intm
        float f = 20.5f; //khai báo biến f là kiểu float
        double d = 20.5; //khai báo biến d là kiểu double
        boolean b = true; //khai báo biến b là kiểu true
        char c = 'a'; //khai báo biến c là kiểu kí tự a
        String s = "Hà Nội"; //khai báo biến s là kiểu chuỗi
        System.out.println("i = " + i);
        System.out.println("f = " + f);
        System.out.println("d = " + d);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("s = " + s);
    }
}